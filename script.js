const grid = document.getElementById("grid");

/* Crear tarjetas */
articles.forEach((article, index) => {
  const card = document.createElement("article");
  card.className = "card";

  card.innerHTML = `
    <img src="${article.cover}" alt="${article.title}">

    <h4>${article.title}</h4>
  `;

  card.addEventListener("click", () => {
    document.getElementById("modal").classList.add("active");
    document.getElementById("modal-image").src = article.hero;
    document.getElementById("modal-title").textContent = article.title;
    document.getElementById("modal-subtitle").textContent = article.subtitle;
    document.getElementById("modal-text").innerHTML = article.text;
    document.getElementById("modal-author").textContent = article.author;

    card.innerHTML = `
      <img src="${article.cover}" alt="${article.title}">
      <h4>${article.title}</h4>`;
  });

  grid.appendChild(card);
});

/* Cerrar modal */
const modal = document.getElementById("modal");
const closeBtn = document.querySelector(".close");

// con botón
closeBtn.addEventListener("click", () => {
  modal.classList.remove("active");
});

// clicando fondo
modal.addEventListener("click", (e) => {
  if (e.target === modal) {
    modal.classList.remove("active");
  }
});
